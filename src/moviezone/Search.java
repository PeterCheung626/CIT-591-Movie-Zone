    private class MoviePanel extends JPanel {
        JButton poster;
        JLabel title;
        public MoviePanel (Movie m) {
            
            poster = new JButton();
            Image image = null;
            //System.out.println(m.getPoster());
            try {
                //URL url = m.getPoster
                 System.out.println(m.getPoster());
                image = ImageIO.read(new URL(m.getPoster()));
                poster.setIcon(new ImageIcon(image));
                title = new JLabel(m.getTitle());
                
                this.add(poster);
                this.add(title);
            } catch (Exception e) {
                
            }
            
        }
    }
